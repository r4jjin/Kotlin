fun main() {

    println("basic integer operations")
    var x = 5
    var y = 10
    println("Basic arithmetic operations via x=$x & y=$y")
    println("Sum is ${x + y}")
    println("Div is ${x / y}")
    println("Reminder is ${x % y}")
    println()
    println("basic decimal operations- use F i.e floating point data type")
    var d1 = 20F
    var d2 = 4.5F
    println("Decimal results after division is ${d1 / d2}")
    println()
    println("basic double type float handling")
    var x1 = 20.0
    var x2 = 3.0
    println("Double result after division is ${x1 / x2}")

}